public class NumberPrinter {
        public static void main(String[] args) {
            // Print numbers from 0 to 10
            System.out.println("Numbers from 0 to 10:");
            int i = 0;
            while (i <= 10) {
                System.out.print(i + " ");
                i++;
            }
            System.out.println();
            System.out.println("Numbers from 10 to 0:");
            int j = 10;
            while (j >= 0) {
                System.out.print(j + " ");
                j--;
            }
            System.out.println();
        }
    }


